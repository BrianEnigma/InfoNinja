#Prerequisites

The [Trimet] transit tracker plugin requires the "[trimetter]" gem and for you
to provide a [Trimet developer ID][TrimetDevID].  The developer ID is free and
is used by Trimet's web service to throttle an application that (accidentally
or on purpose) hits their web API too hard.

[Trimet]: http://trimet.org/
[trimetter]: https://rubygems.org/gems/trimetter
[TrimetDevID]: http://developer.trimet.org/registration/


